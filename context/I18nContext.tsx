const MERGED: TranslationMap = Object.fromEntries(
  (Object.keys(T) as LangCode[]).map((k) => [k, { ...T[k], ...UI[k] }])
) as TranslationMap;

interface I18nContextType {
  lang: LangCode;
  isRTL: boolean;
  t: (key: string) => string;
  changeLanguage: (code: LangCode) => Promise<void>;
  currentLanguage: Language;
}

const I18nContext = createContext<I18nContextType | null>(null);
const STORAGE_KEY = "@drypskin_language";

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<LangCode>("en");

  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEY).then((saved) => {
      if (saved && T[saved as LangCode]) setLang(saved as LangCode);
    });
  }, []);

  const changeLanguage = async (code: LangCode) => {
    await AsyncStorage.setItem(STORAGE_KEY, code);
    setLang(code);
  };

  const t = (key: string): string => {
    return MERGED[lang]?.[key] ?? MERGED.en?.[key] ?? key;
  };

  const currentLanguage = LANGUAGES.find((l) => l.code === lang) ?? LANGUAGES[0];
  const isRTL = currentLanguage.rtl ?? false;

  return (
    <I18nContext.Provider value={{ lang, isRTL, t, changeLanguage, currentLanguage }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
